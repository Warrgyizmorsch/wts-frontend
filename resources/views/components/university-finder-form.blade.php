<script>
    (function () {
        const wrapper = document.querySelector('.new-home-form-stages');
        const stages = wrapper.querySelectorAll('.new-home-form-stage');
        const progressSteps = document.querySelectorAll('.new-home-form-progress div');
        const nextBtn = document.getElementById('nextBtn');
        const prevBtn = document.getElementById('prevBtn');
        let current = 0;

        function updateProgress() {
            progressSteps.forEach((p, i) => p.classList.toggle('active', i <= current));
            prevBtn.disabled = current === 0;
            nextBtn.textContent = current === stages.length - 1 ? 'Submit' : 'Next';
        }

        function showStage(next) {
            stages.forEach(s => s.classList.remove('active'));
            stages[next].classList.add('active');
            wrapper.style.height = stages[next].offsetHeight + 'px'; // smooth height if needed
            current = next;
            updateProgress();
        }

        nextBtn.onclick = () => {
            if (current < stages.length - 1) showStage(current + 1);
            else alert('Form submitted!');
        };
        prevBtn.onclick = () => current > 0 && showStage(current - 1);

        document.querySelectorAll('.new-home-form-option').forEach(opt => {
            opt.addEventListener('click', () => {
                const parent = opt.parentElement;
                parent.querySelectorAll('.new-home-form-option').forEach(o => o.classList.remove('selected'));
                opt.classList.add('selected');
            });
        });

        // Initial height
        wrapper.style.height = stages[0].offsetHeight + 'px';
    })();

</script>